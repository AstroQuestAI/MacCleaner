import os
import time
from pathlib import Path
from maccleaner.scanners.node_modules_scanner import NodeModulesScanner
from maccleaner.models.category import Category


def _make_project(root: Path, name: str, with_package_json: bool = True) -> Path:
    project = root / name
    project.mkdir(parents=True)
    if with_package_json:
        (project / "package.json").write_text('{"name":"test"}')
    nm = project / "node_modules"
    nm.mkdir()
    (nm / "some_dep").mkdir()
    (nm / "some_dep" / "index.js").write_bytes(b"x" * 1024)
    return project


class TestNodeModulesScanner:
    def test_given_no_projects_when_scan_then_empty(self, tmp_path: Path):
        result = NodeModulesScanner(home=tmp_path, unused_days=0).scan()
        assert result.count == 0
        assert result.category == Category.UNUSED_NODE_MODULES

    def test_given_old_project_with_node_modules_when_scan_then_found(self, tmp_path: Path):
        projects = tmp_path / "Projects"
        project = _make_project(projects, "old_app")
        old_time = time.time() - 40 * 86_400
        os.utime(project, (old_time, old_time))

        result = NodeModulesScanner(home=tmp_path, unused_days=30).scan()
        assert result.count == 1
        assert result.entries[0].path.name == "node_modules"

    def test_given_recent_project_when_scan_then_excluded(self, tmp_path: Path):
        projects = tmp_path / "Projects"
        _make_project(projects, "active_app")
        # project mtime is NOW — not old enough

        result = NodeModulesScanner(home=tmp_path, unused_days=30).scan()
        assert result.count == 0

    def test_given_no_package_json_when_scan_then_excluded(self, tmp_path: Path):
        projects = tmp_path / "Projects"
        project = _make_project(projects, "orphan_app", with_package_json=False)
        old_time = time.time() - 40 * 86_400
        os.utime(project, (old_time, old_time))

        result = NodeModulesScanner(home=tmp_path, unused_days=30).scan()
        assert result.count == 0

    def test_given_nested_project_when_scan_then_found(self, tmp_path: Path):
        # monorepo: Projects/monorepo/packages/app/node_modules
        projects = tmp_path / "Projects"
        monorepo = projects / "monorepo"
        app = _make_project(monorepo / "packages", "app")
        old_time = time.time() - 40 * 86_400
        os.utime(app, (old_time, old_time))  # backdate the immediate project dir

        result = NodeModulesScanner(home=tmp_path, unused_days=30).scan()
        assert result.count >= 1
