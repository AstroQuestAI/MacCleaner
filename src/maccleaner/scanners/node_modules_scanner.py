"""Unused node_modules scanner.

Finds node_modules directories inside common project roots where the parent
project hasn't been touched in 30+ days. node_modules can always be recreated
with `npm install`, so deletion is safe as long as package.json is present.
"""
import time
from pathlib import Path

from ..models.category import Category
from .base import BaseScanner

_SEARCH_ROOTS = [
    "Projects", "Developer", "code", "repos", "workspace",
    "Documents", "Desktop", "Downloads", "Sites", "src",
]

_UNUSED_DAYS = 30
_SECONDS_PER_DAY = 86_400
_MAX_DEPTH = 4  # how many directory levels to search within each root


def _days_since_modified(path: Path) -> float:
    try:
        return (time.time() - path.stat().st_mtime) / _SECONDS_PER_DAY
    except OSError:
        return 0.0


def _find_node_modules(directory: Path, max_depth: int) -> list[Path]:
    """Recursively find node_modules dirs, not descending into them."""
    results: list[Path] = []
    if max_depth <= 0:
        return results
    try:
        for child in directory.iterdir():
            if not child.is_dir():
                continue
            if child.name == "node_modules":
                # Only include if a package.json sits next to it
                if (directory / "package.json").exists():
                    results.append(child)
                # Never recurse inside node_modules
            else:
                results.extend(_find_node_modules(child, max_depth - 1))
    except (PermissionError, OSError):
        pass
    return results


class NodeModulesScanner(BaseScanner):
    category = Category.UNUSED_NODE_MODULES

    def __init__(self, home: Path | None = None, unused_days: int = _UNUSED_DAYS) -> None:
        self._home = home or Path.home()
        self._unused_days = unused_days

    def _find_paths(self) -> list[Path]:
        found: list[Path] = []
        for root_name in _SEARCH_ROOTS:
            root = self._home / root_name
            if not root.exists():
                continue
            try:
                for project in root.iterdir():
                    if not project.is_dir():
                        continue
                    for nm in _find_node_modules(project, _MAX_DEPTH):
                        parent = nm.parent
                        if _days_since_modified(parent) >= self._unused_days:
                            found.append(nm)
            except (PermissionError, OSError):
                continue
        return found
